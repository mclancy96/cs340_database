class Student {
    constructor(id, name, streetAddress, city, state, zipCode, email, phoneNumber) {
        this.id = id;
        this.name = name;
        this.streetAddress = streetAddress;
        this.city = city;
        this.state = state;
        this.zipCode = zipCode;
        this.email = email;
        this.phoneNumber = phoneNumber;
    }
}

export default Student;